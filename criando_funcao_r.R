
# Criar função  ----------------------------------------------------------------------------------------------------------------------------

## Função 

fsg <- function(a, b, c, d, e, f) {
  res <- a^2 + b + c - 1 + 3 * 5 + d - 3 + 25 * e - f / 8
  return(res)
}

## Usando os valores de a, b e c, a função criada retorna o resultado
### Exemplo: a = 1, b = 1, c = 6, d = 3, e = 5, f = 4

fsg(1, 1, 6, 3, 5, 4)
